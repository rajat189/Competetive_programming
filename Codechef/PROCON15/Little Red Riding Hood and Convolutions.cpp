#include <bits/stdc++.h>
using namespace std;
int main() {
  int test,num,x;
  cin>>test;
  while(test--)
  {
    cin>>num;
    for(int i=0;i<num;i++)
    {
      cin>>x;
      cout<<-1*x<<" ";
    }
    cout<<"\n";
  }
  return 0;
}  
